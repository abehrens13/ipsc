CREATE TABLE STATUS_TYPE
(
   ID SERIAL,
   NAME_EN VARCHAR (50),
   NAME_DE VARCHAR (50),
   NAME_FR VARCHAR (50)
);
CREATE TABLE ORGANISER
(
   ID SERIAL,
   NAME VARCHAR (50),
