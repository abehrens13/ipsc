   DESCRIPTION VARCHAR (200)
);
CREATE TABLE COUNTRIES
(
   ID SERIAL,
   CODE CHAR (3) NOT NULL UNIQUE,
   NAME_EN VARCHAR (50) NOT NULL UNIQUE,
   NAME_DE VARCHAR (50),
   NAME_FR VARCHAR (50)
);
